# TUPLES AND SETS

# list
data_list = [1, 2, 5, 6, 7, 3]
print(data_list)

# tuples
data_tuples = (1, 2, 9, 5, 6, 3)
# tidak bisa diganti salah satu datanya, ditambah, dihilangkan, dsb
print(data_tuples)
# no append
print(data_tuples[1])

# sets (himpunan)
data_sets = {1, 2, 6, 9, 7, 3}
print(data_sets)
# hanya tidak bisa mengambil/menampilkan satu data, seperti ini -> print(data_sets[3])
