# operasi aritmatika

a = 14
b = 3

# tambah +
hasil = a + b
print(a, " + ", b, " = ", hasil)

# kurang - 
hasil = a - b
print(a, " - ", b, " = ", hasil)

# kali *
hasil = a * b
print(a, " * ", b, " = ", hasil)

# bagi /
hasil = a / b
print(a, " + ", b, " = ", hasil)

# modulus %
hasil = a % b
print(a, " % ", b, " = ", hasil)

# pangkat **
hasil = a ** b
print(a, '**', b, '=', hasil)

# floor division //
hasil = a // b
print(a, "//", b, "=", hasil)

# prioritas operasi, 
hasil = a ** b + a - b / a * a // b
print(a, '**', b, '+',a, '-', b, '/', a, '*', a, '//', b, " = ", hasil)